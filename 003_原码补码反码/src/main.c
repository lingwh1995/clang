/**
 * 一、在计算机中，整数(char、int、shot、long)的存储和计算时使用的都是补码，使用补码完美解决了下面几个问题
 * 1. 0的表示唯一性：
 *    - 原码中+0(0000 0000)和-0(1000 0000)是两个不同值，导致0的表示不唯一
 *    - 补码中+0和-0的二进制均为0000 0000，彻底解决0的唯一性问题
 *
 * 2. 减法转加法：
 *    - CPU仅需加法器电路即可处理加减法运算，大幅简化硬件设计
 *    - 本质：a - b = a + (-b)，负数用补码表示后直接参与加法运算
 *
 * 3. 扩展数值范围：
 *    - 8位补码系统中，1000 0000 不再表示-0，而是定义为-128
 *    - 8位补码可表示范围：-128 ~ +127（正数和负数数量均衡）
 *
 * 二、原码/反码/补码转换规则
 * 1. 正数：原码、补码、反码是一样的
 *    5 的二进制
 *    - 原码 => 0000 0101
 *    - 反码 => 0000 0101
 *    - 补码 => 0000 0101
 *
 * 2. 负数：
 *    由原码到补码：原码 => 反码 => 补码
 * 	  - 原码：
 * 	  - 反码：原码的符号位不变，其余位按位取反。
 * 	  - 补码：在反码的基础上加1
 *
 * 	  由补码到原码：补码 => 反码 => 原码
 * 	  - 补码：
 * 	  - 反码：补码的符号位不变，其余位按位取反。
 * 	  - 原码：在反码的基础上加1
 *
 *    示例：-5的 原码 => 反码 => 补码 => 原码按位取反，符号位不变，再加1
 *    - 原码 => 1000 0101
 *    - 反码 => 1111 1010
 *    - 补码 => 1111 1011
 *
 *	  示例：-5的 补码 => 反码 => 原码 => 补码按位取反，符号位不变，再加1
 *    - 补码 => 1111 1011
 *    - 反码 => 1000 0100
 *    - 原码 => 1000 0101
 *
 * 	  注意：针对负数，不管是由原码到补码还是由补码到原码，操作流程都是按位取反再加1
 *
 * 三、补码运算实例（计算机实际运算方式）
 * 1. 使用反码将减法统一转为加法
 *     5 的二进制 => 原码 0000 0101 => 反码 0000 0101 => 补码   0000 0101
 * 	 											               +
 *    -1 的二进制 => 原码 1000 0001 => 反码 1111 1110 => 补码   1111 1111
 *				                                               =
 *    运算结果									    => 补码   0000 0100 => 十进制的4
 *
 * 2. 两个负数相加(先把负数转换为补码，再进行相加) -1 + (-2) = -3
 *
 *    -1 的二进制 => 原码 1000 0001 => 反码 1111 1110 => 补码   1111 1111
 *  													       +
 *    -2 的二进制 => 原码 1000 0010 => 反码 1111 1101 => 补码   1111 1110
 *                                                             =
 *    运算结果									    => 补码 1 1111 1101 => 8位寄存器中舍弃超过8位的部分 => 1111 1101 => 十进制的-3
 */
void print_binary_string(int n)
{
	for(int i = 7; i >= 0; i--)
	{
		printf("%d", (n >> i) & 1);
	}
	printf("\n");
}

