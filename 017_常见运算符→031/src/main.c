/**
 * 自增运算符
 * i++ : 先用后+
 * ++i : 先加后用
 */
void increment_operator()
{
	// i++
	int i = 10;
    printf("i: %d\n", i++);

    // ++i
    i = 10;
    printf("i: %d\n", ++i);

    i = 10;
    int j = ++i; // 等同于  i = 1 + i; j = i;
    printf("j: %d\n", j);
    printf("-------------------------------------\n");
}

/**
 * 三目运算符
 */
void ternary_conditional_operator()
{
	int a, b, max;
	scanf("%d %d", &a, &b);
	max = a > b ? a : b;
	printf("max = %d\n", max);
	printf("-------------------------------------\n");
}

/* 
 * 加等运算符
 */
void add_equal_operator()
{
	int nums[] = { 1, 2, 3, 4, 5 };
	int len = sizeof(nums) / sizeof(nums[0]);
	int sum = 0 ;
	for (int i = 0; i < len; i++)
	{
		sum += nums[i];
	}
	printf("sum = %d\n", sum);
	printf("-------------------------------------\n");
}

