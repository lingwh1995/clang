/**
 * 位运算
 *
 * 1. 在进行位运算时，不足8位(一个字节)时需要左补0，如 8 => 1000 => 0000 1000
 * 2. 参加位运算的操作数必须是整型类型，以补码形式出现，计算结果自然也是补码
 * 3. 左移运算时正数可能会变负数，负数也可能会变正数，主要要看使用什么数据类型接收结果，因为不同的数据类型占据的空间宽度是不同
 *    的，如果是char类型数据byte7是符号位，如果是short类型，byte15是符号位，如果是int类型，byte31是符号位
 *
 * 	  正数10左移 一位到四位 全过程
 * 	  	             二进制数        char类型  short类型
 * 	  原始数据	0000 0000 0000 1010    10       10
 * 	  左移一位	0000 0000 0001 0100    20       20
 * 	  左移两位	0000 0000 0010 1000    40       40
 * 	  左移三位	0000 0000 0101 0000    80       80
 * 	  左移四位	0000 0000 1010 0000    -96	    160
 * 
 * 	  负数-10左移 一位到四位 全过程
 * 					 二进制数        char类型  short类型
 * 	  原始数据	1111 1111 1111 0110    -10      -10
 * 	  左移一位	1111 1111 1110 1100    -20      -20
 * 	  左移两位	1111 1111 1101 1000    -40      -40
 * 	  左移三位	1111 1111 1011 0000    -80      -80
 * 	  左移四位	1111 1111 0110 0000     96	  	-160
 *			
 * 	  -10左移四位 => 获取补码 => 获取反码 => 获取原码 => 获取十进制数
 *    -10 => 转换为二进制 => 1111 1111 1111 0110 => 左移四位(得到负数补码) => 1111 1111 0110 0000 => 补码-1得到反码 => 1111 1111 0101 1111
 * 					=> 符号位不变，其他位取反得到原码 => 1000 0000 1010 0000 => 使用short类型接收(byte15为符号位) => 1010 0000 * - 1 =>  -160
 */	
void bitwise_operator()
{
	/**
	 * a =  15	原码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 *  		反码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 *  		补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 *
	 * b = -17	原码 => 1000 0000 0000 0000 0000 0000 0001 0001
	 * 			反码 => 1111 1111 1111 1111 1111 1111 1110 1110
	 * 			补码 => 1111 1111 1111 1111 1111 1111 1110 1111
	 *
	 * c =  10	原码 => 0000 0000 0000 0000 0000 0000 0000 1010
	 * 			反码 => 0000 0000 0000 0000 0000 0000 0000 1010
	 * 			补码 => 0000 0000 0000 0000 0000 0000 0000 1010
	 */
	int a = 15, b = -17, c = 10;
	printf("a = %d, b = %d\n", a, b);

	/**
	 * 一、与运算(& / AND)
	 * 1. 运算规则
	 * 	  - 全1则1，有0则0
	 *
	 * 2. 运算原理
	 *    - & 0：将二进制指定位 复位为 0
     *  		 1 & 0 => 0
	 *  		 0 & 0 => 0
	 *    - & 1：将二进制指定位 保留不变
     *  		 1 & 1 => 1
	 *  		 0 & 1 => 0
	 *
	 * 3. 操作方式
	 *    - 要复位为0的位二进制值为0，其他位二进制值为1
	 *
	 * 4. 运算示例
	 *      15 & -17
	 *      a =  15 补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 														     &
	 *      a = -17 补码 => 1111 1111 1111 1111 1111 1111 1110 1111
	 * 														     =
	 * 	   	        补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 	   	        反码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 	   	        原码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 	   	        结果 => 15
	 *
	 * 5. 应用场景
	 *    - 迅速清零 => a & 0
	 *      a =  15  补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 														      &
	 * 		      0  补码 => 0000 0000 0000 0000 0000 0000 0000 0000
	 * 														      =
	 * 	    	     补码 => 0000 0000 0000 0000 0000 0000 0000 0000
	 * 	    	     反码 => 0000 0000 0000 0000 0000 0000 0000 0000
	 * 	    	     原码 => 0000 0000 0000 0000 0000 0000 0000 0000
	 *	  		     结果 => 0
	 *
	 *    - bit1复位为0 => a & 0xFFFFFFFD
	 *      a =  15  补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 														      &
	 * 		      0  补码 => 1111 1111 1111 1111 1111 1111 1111 1101
	 * 														      =
	 * 	    	     补码 => 0000 0000 0000 0000 0000 0000 0000 1101
	 * 	    	     反码 => 0000 0000 0000 0000 0000 0000 0000 1101
	 * 	    	     原码 => 0000 0000 0000 0000 0000 0000 0000 1101
	 *	  		     结果 => 13
	 *
	 *    - 保留指定位 => a & 6，将二进制指定位 保留不变(这里保留bit1、bit2)
	 *      a =  15  补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 														      &
	 *            6  补码 => 0000 0000 0000 0000 0000 0000 0000 0110
	 * 														      =
	 * 	    	     补码 => 0000 0000 0000 0000 0000 0000 0000 0110
	 * 	    	     反码 => 0000 0000 0000 0000 0000 0000 0000 0110
	 * 	    	     原码 => 0000 0000 0000 0000 0000 0000 0000 0110
	 *               结果 => 6
	 *
	 *    - 判断奇偶 => a & 1
	 *      a =  15  补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 														      &
	 * 		      1  补码 => 0000 0000 0000 0000 0000 0000 0000 0001
	 * 														      =
	 * 	    	     补码 => 0000 0000 0000 0000 0000 0000 0000 0001
	 * 	    	     反码 => 0000 0000 0000 0000 0000 0000 0000 0001
	 * 	    	     原码 => 0000 0000 0000 0000 0000 0000 0000 0001
	 * 	    	     结果 => 1
	 */
	// 与运算运算示例
	printf("a & b = %d\n", a & b);
	// a = a & b; 可以简写为 a &= b;
	// 与运算应用场景
	printf("迅速清零 a & 0 = %d\n", a & 0);
	printf("bit1复位为0 a & 0xFFFFFFFD = %d\n", a & 0xFFFFFFFD);
	printf("保留指定 a & 6 = %d\n", a & 6);
	printf("判断奇偶 a & 1 = %d\n", a & 1);
	printf("判断奇偶 a & 1 = %s\n", a & 1 ? "奇" : "偶");

	/**
	 * 与运算练习1 - 判断 0-9 哪个是奇数，哪个是偶数
	 */
	for(int i = 0; i < 10; i++)
	{
		printf("判断奇偶 %d & 1 = %s\n", i, i & 1 ? "奇" : "偶");
	}

	/**
	 * 与运算练习2 - 判断 0-100 哪些数可以被8整除
	 *
	 * 原理 10 进制中个位为0可以被10整除，个位和百位都为0，可以被 10*10 = 100 整除，个位、百位和千位都为0，可以被 10*10*10 = 1000 整除
	 * 原理  2 进制中bit0为0可以被2整除，bit0和bit1都为0，可以被 2*2 = 4 整除，bit0、bit1和bit2都为0，可以被 2*2*2 = 8 整除
	 * 所以这里我们可以判断 bit0、bit1、bit2是否同时为0，使用与运算保留后 bit0、bit1、bit2 ，这里与 0111(0x07)
	 */
	for(int i = 0; i < 100; i++)
	{
		if((i & 0x07) == 0)
		{
			printf("能被8整除的数 %d\n", i);
		}
	}
	printf("---------\n");


	/**
	 * 二、或运算(| - OR)
	 * 1. 运算规则
	 * 	  - 有1则1，全0则0
	 *
	 * 2. 运算原理
	 *    - | 0：将二进制指定位 保留不变
	 *  		 1 | 0 => 1
	 *  		 0 | 0 => 0
	 *    - | 1：将二进制指定位 置位为 1
	 *  		 1 | 1 => 1
	 *  		 0 | 1 => 1
	 *
	 * 3. 操作方式
	 *    - 要置位为1的位二进制值为1，其他位二进制值为0
	 *
	 * 4. 运算示例
	 *      15 | -17
	 * 		a =  15	补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 				   										     |
	 *      b = -17	补码 => 1111 1111 1111 1111 1111 1111 1110 1111
	 * 				   										     =
	 * 	  		    补码 => 1111 1111 1111 1111 1111 1111 1110 1111
	 * 	  		    反码 => 1000 0000 0000 0000 0000 0000 0001 0000
	 * 	  		    原码 => 1000 0000 0000 0000 0000 0000 0001 0001
	 * 	  		    结果 => -17
	 *
	 * 5. 应用场景
	 *    - bit2置位为1 => c | 4
	 *      c =  10  补码 => 0000 0000 0000 0000 0000 0000 0000 1010
	 * 														      |
	 *            4  补码 => 0000 0000 0000 0000 0000 0000 0000 0100
	 * 														      =
	 * 	    	     补码 => 0000 0000 0000 0000 0000 0000 0000 1110
	 * 	    	     反码 => 0000 0000 0000 0000 0000 0000 0000 1110
	 * 	    	     原码 => 0000 0000 0000 0000 0000 0000 0000 1110
	 *               结果 => 14
	 */
	// 或运算运算示例
	printf("a | b = %d\n", a | b);
	// 或运算应用场景
	printf("c | 4 = %d\n", c | 4);
	printf("---------\n");


	/**
	 * 三、非运算(~ - NOT)
	 * 1. 运算规则
	 * 	  - 1变为0，0变为1
	 *
	 * 2. 运算原理
	 *    - 将二进制指定位取反
	 *
	 * 3. 操作方式
	 *    - 二进制位按位取反
	 *
	 * 4. 运算示例
	 *      a =  15	补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 			   										 	     ~
	 * 				   							         	     =
	 *    			补码 => 1111 1111 1111 1111 1111 1111 1111 0000
	 *    			反码 => 1000 0000 0000 0000 0000 0000 0000 1111
	 *    			原码 => 1000 0000 0000 0000 0000 0000 0001 0000
	 *    			结果 => -16
	 *
	 * 		b = -17	补码 => 1111 1111 1111 1111 1111 1111 1110 1111
	 * 			   										 	 	 ~
	 * 				   							         	 	 =
	 *    			补码 => 0000 0000 0000 0000 0000 0000 0001 0000
	 *    			反码 => 0000 0000 0000 0000 0000 0000 0001 0000
	 *    			原码 => 0000 0000 0000 0000 0000 0000 0001 0000
	 *    			结果 => 16
	 *
	 */
	printf("  ~ a = %d\n", ~a);
	printf("  ~ b = %d\n", ~b);


	/**
	 * 四、异或运算(^ - XOR)
	 * 1. 运算规则
	 * 	  - 相同为0，不同为1
	 *
	 * 2. 运算原理
	 *
	 * 3. 操作方式
	 *    - 要进行的两个二进制位，相同结果为0，不同为结果1
	 *
	 * 4. 运算示例
	 *    - a =  15	补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 			   										 	     ^
	 *      b = -17	补码 => 1111 1111 1111 1111 1111 1111 1110 1111
	 * 				   							         	     =
	 *    			补码 => 1111 1111 1111 1111 1111 1111 1110 0000
	 *    			反码 => 1000 0000 0000 0000 0000 0000 0001 1111
	 *    			原码 => 1000 0000 0000 0000 0000 0000 0010 0000
	 *    			结果 => -32
	 *
	 * 5. 应用场景
	 *    - 高效判断两个整形操作数是否相等
	 *      int a = 10, b = 10;
	 *      if(a == b) {}
	 *      if(a - b == 0) {}
	 *      if((a ^ b) == 0) {}
	 *      if(!(a ^ b)) {}
	 *
	 *    - 不引入其他变量交换两个整形数据
	 *      a ^= b;
	 *      b ^= a;
	 *      a ^= b;
	 *
	 *    - 置位/复位操作(之前置位为1使用 | 操作完成，复位为 0 使用 & 操作完成，现在一个 ^ 就可以完成置位或复位操作)
	 *    	bit2置位为1 => c ^ 4
	 *      c =  10  补码 => 0000 0000 0000 0000 0000 0000 0000 1010
	 * 														      ^
	 *            4  补码 => 0000 0000 0000 0000 0000 0000 0000 0100
	 * 														      =
	 * 	    	     补码 => 0000 0000 0000 0000 0000 0000 0000 1110
	 * 	    	     反码 => 0000 0000 0000 0000 0000 0000 0000 1110
	 * 	    	     原码 => 0000 0000 0000 0000 0000 0000 0000 1110
	 *               结果 => 14
	 *
	 *    	bit1复位为0 => a ^ 2
	 *      a =  15  补码 => 0000 0000 0000 0000 0000 0000 0000 1111
	 * 														      ^
	 * 		      2  补码 => 0000 0000 0000 0000 0000 0000 0000 0010
	 * 														      =
	 * 	    	     补码 => 0000 0000 0000 0000 0000 0000 0000 1101
	 * 	    	     反码 => 0000 0000 0000 0000 0000 0000 0000 1101
	 * 	    	     原码 => 0000 0000 0000 0000 0000 0000 0000 1101
	 *	  		     结果 => 13
	 */
	printf("a ^ b = %d\n", a ^ b);
	// a = a ^ b; 可以简写为 a ^= b;
	printf("c ^ 4 = %d\n", c ^ 4);
	printf("a ^ 2 = %d\n", a ^ 2);
	printf("---------\n");


	/**
	 * 左移运算符 - << : 相当于在原来基础上扩大一倍
	 *
	 * a	8	0000 1000
	 * <<	<<	<<
	 * 1    1   1
	 * =	=	=
	 * 		16	0001 0000
	 */
	printf("%d << 1 = %d\n", a, a << 1);
	printf("-%d << 1 = %d\n", a, -a << 1);

	/**
	 * 右移运算符 - >> : 相当于在原来基础上缩小一倍
	 *
	 * a	8	0000 1000
	 * >>	>>	>>
	 * 1    1   1
	 * =	=	=
	 * 		4	0000 0100
	 */
	printf("%d >> 1 = %d\n", a, a >> 1);
	printf("-%d >> 1 = %d\n", a, -a >> 1);
	printf("-------------------------------------\n");
}

